class Product {
  final String id;
  final String category;
  final String name;
  final String description;
  final String price;
  final String timePeriod;
  final String location;
  final String image;

  Product({
    required this.id,
    required this.category,
    required this.name,
    required this.description,
    required this.price,
    required this.timePeriod,
    required this.location,
    required this.image,
  });
}
